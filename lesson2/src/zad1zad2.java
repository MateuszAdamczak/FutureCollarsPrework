public class zad1zad2 {
    public static void main(String[] args) {

        byte byteExample = 127; // 1 bajt

        short shortExample = 32767; // 2 bajt

        int intExample = 2147483647; // 4 bajt

        long longExample = 1L; // 8 bajt (l)

        double doubleExample = 1.500d; // zmiennoprzecinkowe 8 bajt (d) (15msc)

        float floatExample = 4.5050505f; // zmienooprzecinkowe 4 bajt (f) (6-7msc)

        boolean booleamExample1 = true; // prawda
        boolean booleanExample2 = false; // falsz

        char charExample = 'f'; // wartosci z tablicy ASCII


        System.out.println(byteExample);
        System.out.println(shortExample);
        System.out.println(intExample);
        System.out.println(longExample);
        System.out.println(doubleExample);
        System.out.println(floatExample);
        System.out.println(booleamExample1);
        System.out.println(booleanExample2);
        System.out.println(charExample);
    }
}
